// --- Validate each person's path is internally consistent with the bracket ---
// A winner at round N must be one of the two feeding winners from round N-1 (or a fixture team for R16).
function feedWinners(p, stage, idx){ // teams entering match `idx` (0-based) of `stage`
  if(stage==="R8")  return [p.R16[idx*2], p.R16[idx*2+1]];
  if(stage==="R4")  return [p.R8[idx*2],  p.R8[idx*2+1]];
  if(stage==="R2")  return [p.R4[idx*2],  p.R4[idx*2+1]];
  if(stage==="FINAL")return [p.R2[0], p.R2[1]];
  return null;
}
